    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        boolean data_ganda=false;
        int data = jTable1.getRowCount();
        for(int i=0;i<data;i++){
            if(tabel.getValueAt(i, 0).equals(tf_nib.getSelectedItem())){
                data_ganda = true;
            }
        }
        
        if(data_ganda){
            JOptionPane.showMessageDialog(null, "Data Sudah Dimasukkan Dalam Daftar !");
        }else{
            if(tf_nama.getText().isEmpty()){
                tf_no_peminjaman.requestFocus();
            }else{
                if(tf_judul_buku.getText().isEmpty()){
                    tf_nib.requestFocus();
                }else{
                    String[] tabel_master={tf_nib.getSelectedItem().toString(),tf_judul_buku.getText(),tf_pengarang.getText(),tf_batas_pengembalian.getText(),tf_tanggal_kembali.getText(),tf_denda.getText()};
                    tabel.addRow(tabel_master);

                    tf_nib.setSelectedItem("-");
                    tf_judul_buku.setText(null);
                    tf_pengarang.setText(null);
                    tf_batas_pengembalian.setText(null);
                    tf_tanggal_kembali.setText(null);
                    tf_denda.setText("0");
                    
                    hitung_total_denda();
                }
            }
        }
    }//GEN-LAST:event_tambahActionPerformed

