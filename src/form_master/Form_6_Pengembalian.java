    private void tampil_data_detil_peminjaman() {
        BigDecimal denda_perhari;
        denda_perhari = new BigDecimal("0");
        try {
            com.mysql.jdbc.Connection koneksi = (com.mysql.jdbc.Connection) DriverManager.getConnection("jdbc:mysql://localhost/si_perpus","root","");
            ResultSet re=koneksi.createStatement().executeQuery("SELECT * FROM tbl_3_buku WHERE no_induk_buku = '"+tf_nib.getSelectedItem()+"'");
            while(re.next()){
                tf_judul_buku.setText(re.getString(3));
                tf_pengarang.setText(re.getString(4));
            }
            
            ResultSet re1=koneksi.createStatement().executeQuery("SELECT * FROM tbl_4_transaksi_peminjaman WHERE no_peminjaman = '"+tf_no_peminjaman.getSelectedItem()+"'");
            while(re1.next()){
                tf_batas_pengembalian.setText(re1.getString("batas_pengembalian"));
            }
            
            ResultSet re2=koneksi.createStatement().executeQuery("SELECT * FROM tbl_9_denda");
            while(re2.next()){
                denda_perhari = new BigDecimal(re2.getString("denda_perhari"));
            }
            
            //Hitung Keterlambatan dan Denda
            SimpleDateFormat sdfdate = new SimpleDateFormat("yyyy-MM-dd");
            Date tanggal_sekarang = new Date();
            tf_tanggal_kembali.setText(sdfdate.format(tanggal_sekarang));
            
            try{
                if(tf_tanggal_kembali.getText().isEmpty() || tf_batas_pengembalian.getText().isEmpty()){
                    
                }else{
                    SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
                    Date tanggal_batas_pengembalian = (Date) date.parse(tf_batas_pengembalian.getText());
                    Date tanggal_kembali = (Date) date.parse(tf_tanggal_kembali.getText());                
                    if(tanggal_kembali.getTime() > tanggal_batas_pengembalian.getTime()){
                        long Telat = Math.abs(tanggal_kembali.getTime() - tanggal_batas_pengembalian.getTime());
                        long jumlah_hari_telat = TimeUnit.MILLISECONDS.toDays(Telat);
                        BigDecimal denda_perbuku = denda_perhari.multiply(new BigDecimal(jumlah_hari_telat));
                        tf_denda.setText(""+denda_perbuku);
                    }
                }                                
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Error Hitung Denda !");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Terjadi Kesalahan Tampil Data Buku !");
        }
    }

    private void fungsi_update_jumlah_buku_dan_status() {
        int jumlah_data = jTable1.getRowCount();
        for(int i=0;i<jumlah_data;i++){
            int jumlah_buku=0, tersedia=0, keluar=0;
            try {
                com.mysql.jdbc.Connection koneksi = (com.mysql.jdbc.Connection) DriverManager.getConnection("jdbc:mysql://localhost/si_perpus","root","");
                ResultSet re=koneksi.createStatement().executeQuery("SELECT * FROM tbl_3_buku WHERE no_induk_buku = '"+tabel.getValueAt(i, 0)+"'");
                while(re.next()){
                    jumlah_buku = Integer.parseInt(re.getString("jumlah"));
                    keluar = Integer.parseInt(re.getString("keluar")) - 1;
                    tersedia = jumlah_buku - keluar;
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "Terjadi Kesalahan Tampil Baca Data Jumlah Buku !");
            }
            
            try {
                Connection koneksi = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/si_perpus","root","");
                koneksi.createStatement().executeUpdate("UPDATE tbl_3_buku SET jumlah='"+jumlah_buku+"',tersedia='"+tersedia+"',keluar='"+keluar+"' "
                + "WHERE no_induk_buku = '"+tabel.getValueAt(i, 0)+"'");
            } catch (SQLException ex1) {
               JOptionPane.showMessageDialog(this, "Data Jumlah Gagal Diubah !");
            }
            
            try {
                Connection koneksi = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/si_perpus","root","");
                koneksi.createStatement().executeUpdate("UPDATE tbl_5_detil_peminjaman SET status='Kembali' WHERE no_induk_buku = '"+tabel.getValueAt(i, 0)+"' AND no_peminjaman = '"+tf_no_peminjaman.getSelectedItem()+"'");
            } catch (SQLException ex1) {
               JOptionPane.showMessageDialog(this, "Data Jumlah Gagal Diubah !");
            }
        }
    }

