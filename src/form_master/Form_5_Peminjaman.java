    private void tampil_data_anggota() {
        boolean tanggunan_peminjaman = false;
        try {
            com.mysql.jdbc.Connection koneksi = (com.mysql.jdbc.Connection) DriverManager.getConnection("jdbc:mysql://localhost/si_perpus","root","");
            ResultSet re=koneksi.createStatement().executeQuery("SELECT * FROM tbl_4_transaksi_peminjaman WHERE nis = '"+tf_nis.getSelectedItem()+"'");
            while(re.next()){
                ResultSet re1=koneksi.createStatement().executeQuery("SELECT * FROM tbl_5_detil_peminjaman WHERE no_peminjaman = '"+re.getString("no_peminjaman")+"' AND status ='Dipinjam'");
                if(re1.next()){
                    tanggunan_peminjaman = true;
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan Tampil Data Anggota !");
        }
        
        if(tanggunan_peminjaman){
            JOptionPane.showMessageDialog(null, "Anggota Masih Memiliki \nTanggungan Peminjaman Buku!");
        }else{
            try {
                com.mysql.jdbc.Connection koneksi = (com.mysql.jdbc.Connection) DriverManager.getConnection("jdbc:mysql://localhost/si_perpus","root","");
                ResultSet re=koneksi.createStatement().executeQuery("SELECT * FROM tbl_2_anggota WHERE nis = '"+tf_nis.getSelectedItem()+"'");
                while(re.next()){
                    tf_nama.setText(re.getString(2));
                    tf_kelas.setText(re.getString(3));
                    tf_no_telp.setText(re.getString(4));
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Terjadi Kesalahan Tampil Data Anggota !");
            }
        }        
    }

