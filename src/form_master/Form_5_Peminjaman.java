    private void fungsi_update_jumlah_buku() {
        int jumlah_data = jTable1.getRowCount();
        for(int i=0;i<jumlah_data;i++){
            int jumlah_buku=0, tersedia=0, keluar=0;
            try {
                com.mysql.jdbc.Connection koneksi = (com.mysql.jdbc.Connection) DriverManager.getConnection("jdbc:mysql://localhost/si_perpus","root","");
                ResultSet re=koneksi.createStatement().executeQuery("SELECT * FROM tbl_3_buku WHERE no_induk_buku = '"+tabel.getValueAt(i, 0)+"'");
                while(re.next()){
                    jumlah_buku = Integer.parseInt(re.getString("jumlah"));
                    keluar = Integer.parseInt(re.getString("keluar")) + 1;
                    tersedia = jumlah_buku - keluar;
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Terjadi Kesalahan Tampil Baca Data Jumlah Buku !");
            }
            
            try {
                Connection koneksi = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/si_perpus","root","");
                koneksi.createStatement().executeUpdate("UPDATE tbl_3_buku SET jumlah='"+jumlah_buku+"',tersedia='"+tersedia+"',keluar='"+keluar+"' "
                + "WHERE no_induk_buku = '"+tabel.getValueAt(i, 0)+"'");
            } catch (SQLException ex1) {
               JOptionPane.showMessageDialog(this, "Data Jumlah Gagal Diubah !");
            }
        }
    }
