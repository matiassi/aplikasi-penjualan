    private void penomoran_otomatis() {
        int no_no_peminjaman=0;
        String temp_no_peminjaman="";
        try {
            koneksi();
            String sql = "select no_peminjaman from tbl_4_transaksi_peminjaman order by no_peminjaman desc limit 1";                        
            ResultSet res=stt.executeQuery(sql);
            while (res.next()) {
                temp_no_peminjaman = res.getString(1);                
                String nomor_blkg = temp_no_peminjaman.substring(4,7);
                no_no_peminjaman=Integer.parseInt(nomor_blkg);
            }
                        
            no_no_peminjaman++;
            String nilai=String.valueOf(no_no_peminjaman);                      
            if(nilai.length()==1){
                tf_no_peminjaman.setText("PJM000"+no_no_peminjaman);
            }else
            if(nilai.length()==2){
                tf_no_peminjaman.setText("PJM00"+no_no_peminjaman);
            }else
            if(nilai.length()==3){
                tf_no_peminjaman.setText("PJM0"+no_no_peminjaman);
            }else
            if(nilai.length()==4){
                tf_no_peminjaman.setText("PJM"+no_no_peminjaman);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi Error Penomoran !");
        }
    }

    private void fungsi_simpan_data() {
        boolean simpan_transaksi=false, simpan_detil=false;
        try {
            Connection koneksi = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/si_perpus","root","");
            koneksi.createStatement().executeUpdate("INSERT INTO tbl_4_transaksi_peminjaman VALUES('"+tf_no_peminjaman.getText()+"'"
            + ",'"+tf_nis.getSelectedItem()+"','"+tf_tanggal_pinjam.getText()+"','"+tf_batas_pengembalian.getText()+"'"
            + ",'"+tf_jumlah_buku_dipinjam.getText()+"')");
            simpan_transaksi = true;
        } catch (SQLException ex) {
            simpan_transaksi = false;
        }
        
        int jumlah_data = jTable1.getRowCount();
        for(int i=0;i<jumlah_data;i++){
            try {
                Connection koneksi = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/si_perpus","root","");
                koneksi.createStatement().executeUpdate("INSERT INTO tbl_5_detil_peminjaman VALUES('"+tf_no_peminjaman.getText()+"'"
                + ",'"+tabel.getValueAt(i, 0)+"','1','Dipinjam')");
                simpan_detil = true;
            } catch (SQLException ex) {
                simpan_detil = false;
            }
        }
        
        if(simpan_transaksi && simpan_detil){
            JOptionPane.showMessageDialog(null, "Data Peminjaman Disimpan");
        }else{
            JOptionPane.showMessageDialog(null, "Data Peminjaman Gagal Disimpan");
            if(simpan_detil == false){
                JOptionPane.showMessageDialog(null, "Simpan Detil Gagal");
            }else
            if(simpan_transaksi == false){
                JOptionPane.showMessageDialog(null, "Simpan Transaksi Gagal");
            }else{
                JOptionPane.showMessageDialog(null, "Simpan Gagal");
            }
        }
    }

